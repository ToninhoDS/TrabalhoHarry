                <h1 class="h1zuero">
                        Livro 3: Harry Potter e a Prisioneiro de Azkaban</h1>
                        <p>É o terceiro livro, tanto em ordem de lançamento quanto em ordem cronológica. Dessa vez, o vilão é Sirius Black, que com apenas um feitiço matou 13 pessoas e que fugiu da prisão de Azkaban. E, agora livre, vai atrás de Harry Potter com o objetivo de matá-lo.

﻿

                                Durante a busca por respostas da origem de Sirius Black, Harry e seus fiéis amigos descobrem tenebrosos segredos envolvendo Sirius e a sua família.
                                
                                ﻿
                                
                                Originalmente publicado em 08 de julho de 1999, possui 352 páginas e chegou no Brasil em 1º de dezembro de 2000. Assim como os dois livros antecessores, vendou bastante, mais precisamente 55 milhões de cópias.
                               
                                </p>
