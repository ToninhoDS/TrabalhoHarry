            <h1>A História de Harry Potter</h1>
            <p>A história se desenvolve em volta do protagonista, Harry James (ou Tiago, no Brasil) Potter, um garoto órfão que aos 11 anos descobre que é um bruxo ao ser surpreendentemente convidado a estudar na Escola de Magia e Bruxaria de Hogwarts. O arco principal da história gira em torno da amizade de Harry com Hermine Granger e Ronald Weasley, além do diretor de Hogwarts, Alvo Dumbledore. Claro há um grande vilão: Lord Voldemort - "aquele-que-não-deve-ser-nomeado".

﻿
                    Harry "cai de paraquedas" no universo bruxo e não entende a admiração e respeito que muitos tem por ele, muito menos o ódio que Lord Voldemort. Ao passar dos livros, conhecemos a origem de Harry e o amadurecimento dele como bruxo, sempre com o objetivo que liquidar Voldemort, e vice-e-versa.</p>

