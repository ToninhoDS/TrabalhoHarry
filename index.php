                                            <h1 class="h1zuero">Livro 6: Harry Potter e o Enigma do Príncipe</h1>
                                            <p>Há muito tempo, Voldemort descobriu um feitiço que o tornaria imortal: A Maldição das Horcruxes. Esta maldição consiste em objetos que um bruxo deposita partes de sua alma para continuar vivendo, mesmo que seu corpo seja destruído.

﻿

                                                    Aquele-que-não-deve-ser-nomeado dividiu sua alma em 7, criando 6 horcruxes. O Harry Potter e o Dumbledore já descobriram duas: o diário do Tom Riddle e o anel dos Gaunt.
                                                    
                                                    ﻿
                                                    
                                                    Harry acha um livro de Poções, que pertencia ao Príncipe Mestiço, fazendo com que ele, misteriosamente, vire o melhor da matéria. Melhor que a sua amiga nerd, Hermione. Dumbledore, na busca pelas horcruxes, fica bastante debilitado e pede para que Harry continue a busca indo atrás de Snape.
                                                    
                                                    ﻿
                                                    
                                                    Quando Dumbledore está  saindo da torre, ele é paralisado. Então, Draco Malfoy aponta uma varinha para ele. Surge Snape e mata o Diretor. Neste momento começa a  perseguição atrás do Snape, que revela que é o Príncipe Mestiço!
                                                    
                                                    ﻿
                                                    
                                                    O ano acaba e o Harry Potter fala que não volta mais pra Hogwarts. Agora, é o momento de ir atrás de  Lord Voldemort.
                                                    
                                                    ﻿
                                                    
                                                    Harry Potter e o Enígma do Príncipe possui 471 páginas e foi lançado no dia 15 de julho de 2005 (Reino Unido) e no dia 26 de novembro de 2005 no Brasil. Vendeu, até o momento, 65 milhões de exemplares.</p>
                                       
