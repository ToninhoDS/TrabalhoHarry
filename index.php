                                    <h1 class="h1zuero">Livro 5: Harry Potter e a Orden da Fênix</h1>
                                    <p>O quinto livro da série narra os desafios de Harry e seus amigos  no quinto ano em Hogwarts, que incluem a aparição, novamente, do vilão Lord Voldemort e a preparação para os Níveis Ordinários em Magia, testes realizados por alunos do quinto ano.

﻿﻿

                                            Após presenciar o ressurgimento de Voldemort e lutar com ele um ano antes, dessa vez, antes do ano letivo começar, Harry salva o seu primo do ataque de dois Dementadores, porém, ao usar a magia fora da Escola, o Ministério da Magia comunica a exclusão do bruxo de Hogwarts. A decisão, porém, foi alterada para um julgamento disciplinar. Harry é absolvido.
                                            
                                            ﻿﻿
                                            
                                            Então, Harry vai até a casa onde Sírius Black está se escondendo e lá conhece a Ordem da Fenix, criada para se livrar de Valdemort. Ao regressar na Escola, Harry se depara com uma nova professora, Dolores Umbridge, que supostamente ensina a matéria de Defesa Contra a Arte das Trevas. Harry e seus amigos criam a Armada de Dumbledore, para ensinar, de fato, esta matéria.
                                            
                                            ﻿﻿
                                            
                                            Em determinado momento, Harry tem um sonho de que deve ir até o Ministério da Magia. Chegando até o local, percebem que tudo era uma armadilha dos Comensais da Morte. Uma guerra se instaura e que acaba com a morte de Sírius Black.
                                            
                                            ﻿﻿
                                            
                                            O livro possui 509 páginas e foi lançado em 21 de junho de 2003 no Reino Unido e, no Brasil em 29 de novembro do mesmo ano. Também teve vendagem absurda, 55 milhões de cópias.</p>
                                  
