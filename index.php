    <h1 class="h1zuero">A origem do universo de Harry Potter</h1>
    <p align="justify">A ideia do universo de Harry Potter surgiu de forma repentina, em 1990, como a própria autora afirma. Assim que chegou em casa, após a viagem de trem inspiradora, começou a escrever. Porém, sua mãe veio a falecer em dezembro do mesmo ano. Este fato afetou bastante a escrita de J.K. O fato de Harry sofrer com a ausência dos pais está diretamente relacionado com a realidade vivida em época por Jo.

            J.K. revelou em seu site:           
            "Tenho escrito continuamente desde os seis anos de idade mas nunca estive tão excitada com uma ideia antes. [...] Eu simplesmente sentei e pensei, por quatro horas (trem atrasado), e todos os detalhes borbulharam em meu cérebro e este garoto de óculos e cabelos pretos que não sabia que era um bruxo tornou-se mais e mais real para mim".
                       Naquele momento, a sua vida não estava fácil. Sua filha, Jessica Isabel Rowling Arantes, havia nascido em 1993, um ano após se casar com Jorge Arantes, um jornalista português. O casamento durou pouco tempo e coube a Jo cuidar da filha. Mesmo com apenas alguns capítulos do livro escritos, as duas se mudaram para Edimburgo, para que pudessem ficar próximas à irmã de J.K.
                       Desempregada, desiludida com a sua carreira acadêmica e sofrendo com a depressão - doença que inspirou a criação dos Dementadores, seres que sugam sua alma, introduzidos no terceiro livro - coube a ela dedicar à sua única esperança, terminar de escrever Harry Potter. E assim fez, em 1995.
            O caminho pelo reconhecimento não foi fácil. Antes de ser publicado, o livro foi recusado por 12 editoras! Após um ano de tentativas frustradas, a obra foi aprovada pela editora Bloomsburry, de Londres, através do editor Barry Cunningham. A publicação, todavia, deve-se muito a Alice Newton, filha de oito de anos do presidente da editora, que leu o primeiro capítulo e  exigiu a sequência para o seu pai.
            Bloomsburry publicou Harry Potter e a Pedra Filosofal em 1997 com uma tiragem inicial de 1.000 cópias, das quais 500 foram destinadas à bibliotecas escolares. Meses mais tarde, ainda em 1997, J.K. Rowling recebeu uma concessão de 8 mil libras da Scottish Arts Council - um órgão escocês governamental para incentivo das artes - para dar continuidade a série de livros.
            Os meses seguintes foram de gratas surpresas para a autora. Prêmios e mais prêmios foram conquistados, com traduções para diversas línguas e milhões de cópias vendidas no mundo todo.</p>
