                                <h1 class="h1zuero">Livro 4: Harry Potter e o Cálice de Fogo</h1>
                                <p>O livro começa com Harry tendo um pesadelo, onde assiste Lord Voldemort matar Frank Bryce. Depois, os Weasleys buscam Harry e Hermione para assistirem à partida entre Irlanda e Bulgária pela Copa Anual de Quadribol - esporte praticado pelos bruxos em cima de suas vassouras voadoras, que tem como objetivo de pegar o Pomo de Ouro -. Após a partida, Comensais da Morte, seguidores de Voldemort, atacam o local destruindo tudo e matando pessoas.

﻿

                                        Já em Hogwarts, Dumbledore anuncia que a Escola participará do Torneio Tribruxo, que teve a sua última realização 202 anos atrás.  A disputa é entre as três maiores escolas bruxas da Europa: Hogwarts, Beauxbatons e Durmstrang. Para participar do Torneiro Tribruxo, os alunos devem ter mais de 17 anos. E os escolhidos são selecionados pelo Cálice de Ouro, que estranhamente seleciona Harry, então com 14 anos e ele é forçado a competir. O torneio ocorre ao longo de um ano, com diversas provas que envolvem até dragões.
                                        
                                        ﻿
                                        
                                        Mais uma vez, Voldemort aparece, envolve os seus Comensais da Morte e ainda afirma que possui em Hogwarts um "servo fiel", que estava trabalhando para garantir que Harry morresse. Harry e Voldemort se enfrentam com suas varinhas. Durante a batalha, suas varinhas se conectam, por possuir o mesmo núcleo.
                                        
                                        ﻿
                                        
                                        O livro possui 591 páginas e, assim como o seu antecessor, Harry Potter e o Cálice de Fogo vendeu cerca de 55 milhões de cópias. Foi lançado em 08 de julho de 2000 nos Estados Unidos e no Reino Unido. No Brasil, foi lançado em junho de 2001.</p>
