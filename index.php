                                                    <h1 class="h1zuero">
                                                            Livro 7: Harry Potter e as Relíquias da Morte</h1>
                                                            <p>Dumbledore está morto, mas deixa alguns artefatos no seu testamento: Rony recebe o Desiluminador, que acende ou apaga todas as luzes do ambiente. A Hermione é agraciada com  um livro de histórias infantis e Harry, o pomo de ouro do seu primeiro jogo de Quadribol.

﻿﻿

                                                                    Porém, o objetivo ainda é ir atrás das horcruxes. Encontram outra: um camafeu que tava com a Dolores Umbridge. Durante uma noite qualquer, uma luz guia o Harry Potter para fora da tenda e ele acha a espada da Grifinória dentro de um lago congelado. Ele tenta alcançá-la, mas o camafeu começa a sufocá-lo e Rony o salva usando o Desiluminador.
                                                                    
                                                                    ﻿﻿
                                                                    
                                                                    Aparentemente inútil, o livro que Hermione ganhou tinha um símbolo que os fazem descobrir sobre as Relíquias da Morte: uma capa da invisibilidade, uma pedra que ressuscita os mortos e uma varinha invencível. Estes três itens já eram de conhecimento  deles. Afinal, Harry tinha a capa, supostamente a pedra filosofal que estava no pomo de ouro e a varinha deveria ser a Dumbledore.
                                                                    
                                                                    
                                                                    
                                                                    Porém, Voldemort é mais rápido e pega a varinha de dentro do túmulo. Os três amigos são capturados e Dobby, o elfo querido, os ajuda a fugir, mas acaba morrendo.
                                                                    
                                                                    ﻿
                                                                    
                                                                    Os três descobrem uma horcrux no cofre da Bellatrix, a Taça da Lufa-Lufa. Harry lembra  que outra horcrux está numa sala secreta de Hogwarts, o Diadema da Corvinal. Das que já tem, eles as destróem. Então, vão atrás da serpente do Voldemort, a Nagini.
                                                                    
