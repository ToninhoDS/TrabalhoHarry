                        <h1 class="h1zuero">Livro 1: Harry Potter e a Pedra Filosofal</h1>
                        <p>
                        O primeiro livro conta justamente o início da descoberta de Harry como bruxo. O garoto vive com seus tios e seu primo chato para caral**, sendo muito mal tratado pelos três (dormindo em um quartinho debaixo da escada). Além disso, convive com a constante falta dos seus pais biológicos. Harry não sabe muito da sua origem, já que fora deixado na porta da casa dos tios quando tinha apenas alguns meses de vida.

﻿﻿
                            Tudo muda quando Harry começa a receber cartas convite para ingressar em Hogwarts. Seus tios sempre rasgam e as jogam fora, mas com o passar do tempo o volume de cartas aumenta até o momento que milhares de cartas entram voando na casa dos Dursley.
                            
                            ﻿﻿
                            
                            Os tios de Harry, Válter e Petúnia Dursley, sabem da origem do garoto e que ele é um bruxo, já que a sua mãe, Lilly Potter, uma bruxa nascida "trouxa", é irmã mais nova de Petúnia. Claro, seus tios tentam impedir que Harry vá para a Escola, mas não conseguem, já que o grandalhão Rúbeo Hagrid resgata o garoto.﻿
                            
                            ﻿
                            
                            Logo no embarco para a escola, através da plataforma 9 3/4, Harry conhece seus fiéis amigo, Rony e Hermione. Os três se envolvem em uma série de situações relacionadas a um objeto escondido nas profundezas do castelo de Hogwarts: a pedra filosofal, um artefato mágico capaz de fazer qualquer metal se transformar em ouro puro e, também, produzir o elixir da vida eterna.
                            
                            ﻿                           
                            Durante a narrativa, os três passam a suspeitar que o professor Severo Snape estava tentando roubar a pedra filosofal para entregá-la a Voldemort.﻿
                            Harry Potter e a Pedra Filosofal é o livro com maior vendagem da série, com mais de 107 milhões de cópias comercializadas. E, até o momento, figura como 5º livro mais vendido da história. Possui 255 páginas, o menor deles, e foi lançado no Brasil, pela Rocco em 1º de janeiro de 2000.</p>
               <br>
               <br>
               <br>
               <br>
               <h1 class="h1zuero">Livro 2: Harry Potter e a Câmara Secreta</h1>
               <p>O segundo livro da série é sobre uma câmara secreta localizada dentro de Hogwarts que, supostamente, tem um monstro - o réptil Basilisco - e que matará todos os bruxos que não provém de  famílias mágicas. Vários alunos aparecem petrificados e Harry Potter é apontado como suspeito. Ao mesmo tempo, ele e seus amigos Rony e Hermione tentam acessar a câmara secreta para desvendar o mistério.

﻿﻿

                    Jo confessou que ficou bastante apreensiva com o segundo livro da série, com medo de desapontar o público, devido ao enorme sucesso do livro de estreia. Boa parte da história já estava pronta desde a publicação do seu antecessor e ela o considera um de seus favoritos.
                    
                    ﻿﻿
                    
                    Lançado em 02 de julho de 1998 (agosto de 2000 no Brasil), possui 327 páginas e também foi um sucesso de vendagem, com aproximadamente 60 milhões de exemplares comercializados.</p>
